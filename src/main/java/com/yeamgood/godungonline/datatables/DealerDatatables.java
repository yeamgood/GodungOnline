package com.yeamgood.godungonline.datatables;

public class DealerDatatables {
	
	private String dealerIdEncrypt;
	
	private String supplierName;
	
	private String startDate;
	
	private String endDate;
	
	private String price;
	
	private String measureName;
	
	private String currencyName;

	public String getDealerIdEncrypt() {
		return dealerIdEncrypt;
	}

	public void setDealerIdEncrypt(String dealerIdEncrypt) {
		this.dealerIdEncrypt = dealerIdEncrypt;
	}

	public String getSupplierName() {
		return supplierName;
	}

	public void setSupplierName(String supplierName) {
		this.supplierName = supplierName;
	}

	public String getStartDate() {
		return startDate;
	}

	public void setStartDate(String startDate) {
		this.startDate = startDate;
	}

	public String getEndDate() {
		return endDate;
	}

	public void setEndDate(String endDate) {
		this.endDate = endDate;
	}

	public String getPrice() {
		return price;
	}

	public void setPrice(String price) {
		this.price = price;
	}

	public String getMeasureName() {
		return measureName;
	}

	public void setMeasureName(String measureName) {
		this.measureName = measureName;
	}

	public String getCurrencyName() {
		return currencyName;
	}

	public void setCurrencyName(String currencyName) {
		this.currencyName = currencyName;
	}
	
}
