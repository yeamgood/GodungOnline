package com.yeamgood.godungonline.datatables;

public class DocumentDatatables {

	private String documentIdEncrypt;
	
    private String name;
     
    private String description;
     
    private String type;
    
    private String size;

	public String getDocumentIdEncrypt() {
		return documentIdEncrypt;
	}

	public void setDocumentIdEncrypt(String documentIdEncrypt) {
		this.documentIdEncrypt = documentIdEncrypt;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public String getDescription() {
		return description;
	}

	public void setDescription(String description) {
		this.description = description;
	}

	public String getType() {
		return type;
	}

	public void setType(String type) {
		this.type = type;
	}

	public String getSize() {
		return size;
	}

	public void setSize(String size) {
		this.size = size;
	}
    	
}
